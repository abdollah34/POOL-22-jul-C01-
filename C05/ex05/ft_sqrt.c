#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	y;

	y = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (1)
	{
		if ((y * y) == nb)
		{
			return (y);
		}
		if (y * y > nb)
		{
			return (0);
		}
		y++;
	}
}
