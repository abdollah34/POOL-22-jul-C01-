char	*ft_strupcase(char *str)
{
	int	t;

	t = 0;
	while (str[t] != '\0')
	{
		if (str[t] >= 'a' && str[t] <= 'z')
			str[t] = str[t] - 32;
		t++;
	}
	return (str);
}
